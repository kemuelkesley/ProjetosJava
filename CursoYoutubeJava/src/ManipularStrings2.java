
public class ManipularStrings2 {

	public static void main(String[] args) {
		
		String x = "Olá mundo, esse é o novo mundo";
		
		System.out.println(x.length());
		System.out.println(x.contains("mundo"));
		System.out.println(x.indexOf("mundo"));
		System.out.println(x.lastIndexOf("novo"));
		System.out.println(x.trim());
		System.out.println(x.substring(9));
		System.out.println(x.equals("esse"));
		
		
		
		
		
		
		
		
		
	}

	 
	
}
